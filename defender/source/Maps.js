    /**
     * @constrctor
     * The Maps Class
     */
    function Maps() {
        this.maps    = document.querySelector(".maps");
        this.total   = document.querySelector(".totalScore");
        this.storage = new Storage(mapsStorage);
        this.mapData = null;
        this.gameMap = null;
    }
    
    /**
     * Transforms a map index to a map name
     * @param {string} code
     * @return {string}
     */
    Maps.prototype.codeToMap = function (code) {
        var map = "classic";
        if (mapsData[code]) {
            return code;
        }
        
        Object.keys(mapsData).some(function (name) {
            if (mapsData[name].index === String(code)) {
                map = name;
                return true;
            }
            return false;
        });
        return map;
    };
        
    /**
     * Saves the Map and Level when starting a new game
     * @param {string} gameMap
     * @param {number} gameLevel
     */
    Maps.prototype.saveMap = function (gameMap, gameLevel) {
        this.gameMap = gameMap;
        this.storage.set("map", gameMap);
        this.storage.set("level", gameLevel);
    };
    
    /**
     * Returns the currently saved map
     * @return {string}
     */
    Maps.prototype.getMap = function () {
        return this.storage.get("map");
    };
    
    /**
     * Returns the currently saved level
     * @return {number}
     */
    Maps.prototype.getLevel = function () {
        return this.storage.get("level");
    };
    
    
    /**
     * Shows the list of maps to select one
     */
    Maps.prototype.display = function () {
        var score, won, element, total = 0, self = this;
        this.maps.innerHTML = "";
        
        Object.keys(mapsData).forEach(function (id) {
            score   = self.storage.get(id + ".score") || 0;
            won     = self.storage.get(id + ".won");
            element = document.createElement("button");
            total  += score;
            
            element.dataset.action = "selectLevel";
            element.dataset.data   = id;
            element.dataset.name   = mapsData[id].name;
            element.dataset.score  = score;
            element.className      = won ? "navButton" : "menuButton";
            element.innerHTML      = mapsData[id].index;
            
            self.maps.appendChild(element);
        });
        
        this.total.innerHTML = "Total: " + total;
    };
    
    /**
     * Saves the new Score and Lives for the given map, if is greater than the old one
     * @param {number} lives
     * @param {number} score
     */
    Maps.prototype.saveScore = function (lives, score) {
        var old = this.storage.get(this.gameMap + ".score"),
            won = lives > 0;
        
        if (!old || old < score) {
            this.storage.set(this.gameMap + ".score", score);
        }
        if (won) {
            this.storage.set(this.gameMap + ".won", 1);
        }
    };
    
    
    
